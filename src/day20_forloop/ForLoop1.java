package day20_forloop;

public class ForLoop1 {

	public static void main(String[] args) {
		//using for loop print "Love Java! ten times
		
		for(int x=1; x<=10; x++) {
			System.out.println("Love Java");
		}
		for(int y=5; y<=15; y++) {
			System.out.println("I love Java");
		}		
		for(int y=20; y>=10; y--) {
			System.out.print(y + ",");
		}		
	}

}
